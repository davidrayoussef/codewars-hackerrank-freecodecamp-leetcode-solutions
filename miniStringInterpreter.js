/*
Create a simple interpreter function that interprets the following commands:
'+' - Increments the memory cell. If it reaches 256, wraps to 0.
'.' - Outputs the ASCII value of the memory cell
*/

function miniStringInterpreter(code, cell = 0) {
  return code
    .split('')
    .reduce((output,command) => {
      switch (command) {
        case '+': cell = (cell + 1) % 256; break;
        case '.': output += String.fromCharCode(cell); break;
      }

      return output;
    }, '');
}

